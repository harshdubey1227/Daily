public class datatypes {
    public static void main(String[] args) {
        char a = 'C';
        char a2 = 'c';
        double b = 2343.42528584584852;
        float c = 10.7f;
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(a == a2);
        System.out.println(a != a2);
        System.out.println(true);
    }
}
